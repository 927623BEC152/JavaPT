package JavaDay3;

public class BasicSwapping {
    public static void main(String[] args) {

        int a = 10;
        int b = 5;
        int v = a;

        System.out.println("Before Swapping: " + a + " " + b);
        a = b;
        b = v;
        ;
        System.out.println("after Swapping: " + a + " " + b);

    }
}
